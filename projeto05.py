# Passos:
# Ligue o primeiro interruptor (vamos chamá-lo de Interruptor A) e deixe-o ligado por alguns minutos (uns 5 minutos). 
# Isso fará com que a lâmpada conectada a ele aqueça.

# Após os 5 minutos, desligue o Interruptor A e ligue o Interruptor B. Deixe o Interruptor B ligado.

# Imediatamente vá até a sala das lâmpadas.

# Ao chegar na sala:

# A lâmpada que está acesa será controlada pelo Interruptor B (o que você deixou ligado).
# A lâmpada que está apagada, mas quente será controlada pelo Interruptor A (o que você desligou após deixá-la ligada por alguns minutos).
# A lâmpada que está apagada e fria será controlada pelo Interruptor C (o que você nunca ligou).